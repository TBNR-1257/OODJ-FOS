    public RunnerPerformance() {
        initComponents();
        setTitle("Runner Performance");
        
        model = new DefaultTableModel(columnNames, 0);
        performancetable.setModel(model);
        
        try (BufferedReader br = new BufferedReader(new FileReader("src/main/java/com/mycompany/fos_app/Data/ratings.txt"))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] values = line.split(";");
                model.addRow(values);
            }      
            
        }catch(IOException e){
            
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading file: "+ e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }
    
    

